package com.tasktop.c2c.server.common.web.client;

import com.google.gwt.i18n.client.LocalizableResource.Generate;
import com.google.gwt.i18n.client.LocalizableResource.GenerateKeys;
import com.google.gwt.i18n.client.Messages;

/**
 * @author michaelnelson (Tasktop Technologies Inc.)
 * 
 */
@GenerateKeys("com.google.gwt.i18n.server.keygen.MethodNameKeyGenerator")
@Generate(format = "com.google.gwt.i18n.rebind.format.PropertiesFormat")
public interface CommonMessages extends Messages {

	@DefaultMessage("An application error occurred")
	String errorApplicationError();

	@DefaultMessage("File Not Found")
	String errorFileNotFound();

	@DefaultMessage("We''re currently performing maintenance")
	String errorPerformingMaintenance();

	@DefaultMessage("Loading...")
	String loading();
}
